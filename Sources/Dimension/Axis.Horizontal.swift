// Typealias for horizontal orientation on 2D+ axes.

extension Axis where N == 2 {
    /// X-axis orientation convention.
    public typealias Horizontal = Dimension.Horizontal
}

extension Axis where N == 3 {
    /// X-axis orientation convention.
    public typealias Horizontal = Dimension.Horizontal
}

extension Axis where N == 4 {
    /// X-axis orientation convention.
    public typealias Horizontal = Dimension.Horizontal
}
